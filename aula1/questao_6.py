""" 6. Exercício do Cálculo de Área: Escreva um programa em Python que solicite ao usuário a largura e a altura de um
retângulo. Calcule a área desse retângulo e exiba o resultado. """

largura = float(input('Digite o valor da largura do retângulo: '))
altura = float(input('Digite o valor da altura do retângulo: '))

area = largura * altura

print('A área do retângulo é: R$ %.2f' %area)